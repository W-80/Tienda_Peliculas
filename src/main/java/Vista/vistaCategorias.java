    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        int fila = tableListaCategorias.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } else {

            crudCategorias objCrudCategorias = new crudCategorias();
            objCrudCategorias.editarCategorias(tableListaCategorias, txtNombreCategoria);
            objCrudCategorias.mostrarCategorias(tableListaCategorias);
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        int fila = tableListaCategorias.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Fila no seleccionada", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } else {

            crudTitulos objCrudTitulos = new crudTitulos();
            crudCategorias objCrudCategorias = new crudCategorias();
            DefaultTableModel modeloTabla = objCrudCategorias.validarEliminacionCategoria(tableListaCategorias);
            String lista = objCrudCategorias.imprimirLista(modeloTabla);
            int confirmacion = JOptionPane.showConfirmDialog(null, "Lo siguiente se asiganar√° a categor√≠a NINGUNO:\n" + lista + "\nEst√° seguro de eliminar el registro seleccionado?", "Eliminar", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                int n = modeloTabla.getRowCount();
                int i = 0;
                while (i < n) {
                    objCrudTitulos.reasignarCategoria(Integer.parseInt(modeloTabla.getValueAt(i, 1).toString()));
                    i = i + 1;
                }
                objCrudCategorias.eliminarCategoria(tableListaCategorias);
                objCrudCategorias.mostrarCategorias(tableListaCategorias);
            } else {
            }
        }
    }//GEN-LAST:event_jButtonEliminarActionPerformed

